// Nokta poligonun içinde mi? (ray casting)
function poligonIcinde(lat, lon, poly) {
  let ic = false;
  for (let i = 0, j = poly.length - 1; i < poly.length; j = i++) {
    const [lati, loni] = poly[i];
    const [latj, lonj] = poly[j];
    if (
      (lati > lat) !== (latj > lat) &&
      lon < ((lonj - loni) * (lat - lati)) / (latj - lati) + loni
    ) {
      ic = !ic;
    }
  }
  return ic;
}

export function turkiyeIcinde(lat, lon) {
  return poligonIcinde(lat, lon, ANADOLU) || poligonIcinde(lat, lon, TRAKYA);
}

// Türkiye sınırları içinde rastgele bir nokta (reddetme örneklemesi)
export function rastgeleNokta() {
  for (let deneme = 0; deneme < 30; deneme++) {
    const lat = 35.95 + Math.random() * 6.1;
    const lon = 26.1 + Math.random() * 18.6;
    if (turkiyeIcinde(lat, lon)) return [lat, lon];
  }
  return [39.0 + Math.random() * 1.5, 32.0 + Math.random() * 6];
}
